const APP_ID = undefined;

const SKILL_NAME = 'Arbitrary Distances to Arbitrary Places';
const GET_FACT_MESSAGE = "Did you know that ";
const HELP_MESSAGE = 'You can say tell me an arbitrary distance, or, you can say exit... What can I help you with?';
const HELP_REPROMPT = 'What can I help you with?';
const STOP_MESSAGE = 'Goodbye!';

//=========================================================================================================================================
//TODO: Replace this data with your own.  You can find translations of this data at http://github.com/alexa/skill-sample-node-js-fact/data
//=========================================================================================================================================
const data = [
    'there are 50389 football fields between Meadowdale, Washington and Cobb Island, Maryland.',
    'there are 8275520 cubits from Gaines, Michigan to El Sobrante, California.',
    'there are 83847561426 twips between Level Green, Pennsylvania to Hiram, Arkansas.',
    'if you want to go from Wyandacnch, New York to Patoka, Indiana it will take you 4.7722089096e-11 parsecs.',
    'the distance between Kilkenny, Minnesota to Ulen, Indiana is 866 Bloits.',
    'there are 5970075 capefoots between Moretown, Vermont and Antioch, Alaska.',
    'the distance between Fellows, California to Owens, Texas is 2207680 human tongues',
    'you can lineup 38195174 American cockroaches between Daily, Colorado and Welch, Oklahoma',
    'it only takes 816971 Shaquille O\'Neals to go from Singer Wisconsen to Melville, Rhode Island.'
];

//=========================================================================================================================================
//Editing anything below this line might break your skill.
//=========================================================================================================================================

const handlers = {
    'LaunchRequest': function () {
        this.emit('GetNewFactIntent');
    },
    'GetNewFactIntent': function () {
        const factArr = data;
        const factIndex = Math.floor(Math.random() * factArr.length);
        const randomFact = factArr[factIndex];
        const speechOutput = GET_FACT_MESSAGE + randomFact;

        this.response.cardRenderer(SKILL_NAME, randomFact);
        this.response.speak(speechOutput);
        this.emit(':responseReady');
    },
    'AMAZON.HelpIntent': function () {
        const speechOutput = HELP_MESSAGE;
        const reprompt = HELP_REPROMPT;

        this.response.speak(speechOutput).listen(reprompt);
        this.emit(':responseReady');
    },
    'AMAZON.CancelIntent': function () {
        this.response.speak(STOP_MESSAGE);
        this.emit(':responseReady');
    },
    'AMAZON.StopIntent': function () {
        this.response.speak(STOP_MESSAGE);
        this.emit(':responseReady');
    },
};

exports.handler = function (event, context, callback) {
    const alexa = Alexa.handler(event, context, callback);
    alexa.APP_ID = APP_ID;
    alexa.registerHandlers(handlers);
    alexa.execute();
};
